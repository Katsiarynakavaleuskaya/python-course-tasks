# #SafeInput â€” the program will not crash on text or empty input

total = 0

# Using float to allow both integers and decimal numbers.
while True:
    # Safe input loop
    while True:
        s = input('Enter a number: ').strip()
        try:
            user_input = float(s)
            break
        except ValueError:
            print('Error! Please enter a number.')

    if user_input == 0:
        break
    total += user_input

print('Result:', total)